class Logger {
    func logDebug(message: String) -> String {
        return "Debug Log: \(message)"
    }
    
    func logInfo(message: String) -> String {
        return "Info Log: \(message)"
    }
    
    func logWarn(message: String) -> String {
        return "Warn Log: \(message)"
    }
    
    func logError(message: String) -> String {
        return "Error Log: \(message)"
    }
}
